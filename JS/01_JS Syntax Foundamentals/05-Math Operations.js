function solve(a,b,txt){
switch(txt){
    case "+": 
    console.log(a+b);
    break;
    case "-": 
    console.log(a-b);
    break;
    case "*": 
    console.log(a*b);
    break;
    case "/": 
    console.log(a/b);
    break;
    case "%": 
    console.log(a%b);
    break;
    case "**": 
    console.log(a**b);
    break;
}
}